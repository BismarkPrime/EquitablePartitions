def oneGraphToRuleThemAll(graph_file: str) -> sp.coo_array:
    """detects the type of input graph. Reads it in and outputs it as a sparse matrix 
    relaying any problems along the way
    PARAMETERS
    ---------------------------------------------
        graph_file (str): name of the file to load into the graph
    """
    tag = graph_file.split('.')[-1]
    if tag == 'csv': 
        h.start_section("CSV FILE DETECTED")
        print("ASSUMPTIONS:\n\twe are assuming that this csv file contains edge data of the form where the first column "
              "is the origin node and the second column is the destination node. The metrics calculated on this graph "
              "will not be accurate if this is false.\n\tWe are assuming the node labels start at 0")
        
        df = pd.read_csv(graph_file)
        # get connections, size, 
        origin = df.iloc[:,0].values
        dest = df.iloc[:,1].values
        # get the node offset (if the nodes are labeled starting at 0 then num_nodes may be innacurate without adjustment)
        node_offset = 1 if 0 in origin or 0 in dest else 0
        num_nodes = max(origin.max(),dest.max()) + node_offset
        prompt = h.parse_input(f"Inferred graph size is {num_nodes}. Is this accurate (yes/no): ")
        # assuming directed but unweighted
        weights = np.ones(origin.size)
        # create the sparse matrix with these values. 'b' is for byte to make the storage EVEN SMALLER!
        G_sparse = sp.coo_array((weights,(origin,dest)),shape=(num_nodes,num_nodes),dtype='b')
    elif tag == 'txt':
        pass

    return G_sparse

def getCharacteristicMatrix(partition):
    """
    Generate the characteristic matrix for an equitable partition.
    Parameters:
        partition (list of sets): The equitable partition.
    Returns:
        The characteristic matrix.
    """
    # change it to a list of sets
    partition = [set(element) for element in partition.values()]
    # create the block characteristic matrix
    n = sum(len(block) for block in partition)
    matrix = np.zeros((n, n))
    i = 0
    for block in partition:
        for j in block:
            matrix[j][i:i+len(block)] = 1
        i += len(block)
    return np.unique(matrix,axis=1)[:,::-1] # get only the unique columns, and reverse so it's in order

def genDivGraph(G,ep_dict,retMat=False):
    """calculates and returns the divisor graph of the input graph
    INPUTS:
        G (networkx graph): the graph whose divisor graph you want
        ep_dict (dict): the dictionary defining the equitable partition on G
        retMat (bool): whether or not the function returns the divisor graph as a matrix
        
    RETURNS:
        divMat (array): the divisor matrix
        OR
        nx.Graph(divMat): divisor matrix as nx graph object
    """
    rev_ep_dict = {node:partEl for partEl in ep_dict.keys() for node in ep_dict[partEl]}
        
    # create empty divisor to fill
    n = len(ep_dict)
    divMat = np.zeros((n,n))
    
    # cycle through one node in each partition element to get connections for the Divisor Graph
    for curPartElInd, node_list in enumerate(ep_dict.values()):
        # always get the first one since the partition is equitable and all connection 
        # will be the same within the partition elements.
        node = node_list[0]

        # count connection to partitions and update divisor matrix accordingly
        for connection in G.edges(node):

            connNode = connection[1]
            divMat[curPartElInd][rev_ep_dict[connNode]] += 1

    # return matrix if desired
    if retMat:
        return divMat
    # otherwise return the divisor graph as a networkx object
    return nx.DiGraph(divMat)

def GetLocalSpec(G,ep_dict,lep_list):
    """return the spectrum of each LEP along with its divisor spectrum in a dictionary"""
    orig_spec = np.round(nx.adjacency_spectrum(G),3)
    GDiv = genDivGraph(G,ep_dict)
    GDivSpec = np.round(nx.adjacency_spectrum(GDiv),3)
    spec_dict = {} # dictionary to hold spectrums
    
    for lep in list(lep_list): # cycle through each lep
        node_list = []   # place to get all nodes in lep
        temp_ep_dict = {} # make a place for the original ep partitions
        
        # iterate through each partition element in that lep.
        for partElInd, partEl in enumerate(lep):
            node_list += ep_dict[partEl] # after this loop node_list has all nodes in the lep
            temp_ep_dict[partElInd] = ep_dict[partEl] # make the temporary ep_dict
        
        subgraph = nx.subgraph(G,node_list) # make a subgraph
        spec_dict[f"{lep}"] = np.round(nx.adjacency_spectrum(subgraph),3)  # store its spectrum in dictionary
       
        spec_dict[f"{lep} Divisor"] = np.round(nx.adjacency_spectrum(genDivGraph(subgraph,temp_ep_dict)),3)
    spec_dict["Original Graph Divisor"] = GDivSpec
        
    return spec_dict, orig_spec

def GenBerthaSparse(n, parallel=False):
    """
    NOTE: this function could be a near-exact copy of GenBertha, but returning `mat` instead of using
    `nx.from_scipy_sparse_array`. However, writing it from scratch may prove more readable and efficient.
    """
    e = 2.36 # exponent of the current fastest algorithm (e.g., we hope that np.eigs is in O(n^e))
    num_leps = int(n ** .5) if parallel else int(n ** (e / (2 * e - 1)))
    lep_size = n // num_leps
    num_larger_leps = n - num_leps * lep_size

    mat = sp.lil_matrix((n, n), dtype=int)
    start = 0
    # width and height are named for the submatrices _under_ the diagonal
    width = lep_size + 1 if num_larger_leps > 0 else lep_size
    height = lep_size + 1 if num_larger_leps > 1 else lep_size
    for i in range(num_leps - 1):
        if i == num_larger_leps:
            width = lep_size
        if i == num_larger_leps - 1:
            height = lep_size
        if i == 0:
            mat[:width, :height] = np.eye(width, dtype=int)
        mat[start:start + width, start + width:start + width + height] = np.ones((width, height), dtype=int)
        mat[start + width:start + width + height, start:start + width] = np.ones((height, width), dtype=int)
        start += width

    # NOTE: for large values of n, it's surprisingly slower (empirically) to fill
    # in one side of the matrix and then use the following to make it symmetric:
    # rows, cols = mat.nonzero()
    # mat[cols, rows] = mat[rows, cols]
    
    return mat

def GenBertha(size,show_graph=False,parallelize=False):
    """ constructs a Bertha graph of the size indicated (size=number of nodes) organized in such a way that it 
    has the optimal amount of LEP's for eigenvalue catching
    INPUTS:
        size (int): number of nodes
        
    RETURNS:
        nx.Graph (diGraph): Bertha as a networkx graph"""
    e = 2.36                                                 # the value of the current fastest algorithm
    opt_lep = int((((e-1)/e) * size**e )**(1 / (2*e - 1)))              # the optimal amount of leps
    # we will generate 2 sizes of leps, size 1, which will be as close to the ceiling of the opt_lep number as possible
    # and size 2, there will only be one lep of size 2 and it will make up the difference. for example if size = 100 then
    # opt_lep = 18, but 100/18 = 5.55555... but we can't have 5.5 nodes per lep so we'll do as many size 5's as we can and then
    # another lep with the leftovers but still get 18 leps, so that would be 17 of size 5 and 1 of size 15    

    # matrix to fill
    mat = sp.lil_matrix((size,size),dtype=int)

    if parallelize:    # then make the function that populates a part of the sparse matrix
        #TODO: figure out how to distribute the populating of the matrix in an efficient way.
        try:
            comm = MPI.COMM_WORLD
            size = comm.getSize()
            rank = comm.getRank()
        except ModuleNotFoundError as e:
            print(f"encountered {e}\n\n Computer must have mpi4py module")

    if not parallelize:
        # get the two sizes
        if size%opt_lep != 0:
            size1 = size//(opt_lep-1)
            size2 = size - size1*(opt_lep-1)
            # populate matrix off diagonals
            for i in range(opt_lep-2):
                mat[(i+1)*size1:(i+2)*size1,i*size1:(i+1)*size1] = np.ones((size1,size1))
                mat = mat.transpose()
                mat[(i+1)*size1:(i+2)*size1,i*size1:(i+1)*size1] = np.ones((size1,size1))

            if size2 != 0:  # if size 2 was not 0 account for last irregularly sized partition element
                mat[(opt_lep-1)*size1:,(opt_lep-2)*size1:(opt_lep-1)*size1] = np.ones((size2,size1))
                mat = mat.transpose()
                mat[(opt_lep-1)*size1:,(opt_lep-2)*size1:(opt_lep-1)*size1] = np.ones((size2,size1))

        else:   # of the optimal lep size was a divisor just do the exact number of optimal leps
            size1 = int(size/opt_lep)

            for i in range(opt_lep-1):
                mat[(i+1)*size1:(i+2)*size1,i*size1:(i+1)*size1] = np.ones((size1,size1))
                mat = mat.transpose()
                mat[(i+1)*size1:(i+2)*size1,i*size1:(i+1)*size1] = np.ones((size1,size1))

        # make bertha a networkx object
        bertha = nx.from_scipy_sparse_array(mat)

        if show_graph:
            nx.draw_networkx(bertha)
            plt.show()

        return bertha

        

