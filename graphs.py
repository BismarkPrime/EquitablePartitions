def genDivGraph(G,ep_dict,retMat=False):
    """calculates and returns the divisor graph of the input graph
    INPUTS:
        G (networkx graph): the graph whose divisor graph you want
        ep_dict (dict): the dictionary defining the equitable partition on G
        retMat (bool): whether or not the function returns the divisor graph as a matrix
        
    RETURNS:
        divMat (array): the divisor matrix
        OR
        nx.Graph(divMat): divisor matrix as nx graph object"""
    # helper function to get key given a value
    def get_key(val,my_dict):
        for key, value_list in list(my_dict.items()):
            if val in value_list:
                return key
        print("they key does not exist")
        
    # create empty divisor to fill
    n = len(ep_dict)
    divMat = np.zeros((n,n))
    
    # cycle through one node in each partition element to get connections for the Divisor Graph
    for curPartElInd, node_list in enumerate(ep_dict.values()):
        # always get the first one since the partition is equitable and all connection 
        # will be the same within the partition elements.
        node = node_list[0]
        #print(f"this is the input node: {node}") # debugging statement
        # count connection to partitions and update divisor matrix accordingly
        for connection in G.edges(node):
            connNode = connection[1]
            #print(curPartElInd,get_key(connNode,ep_dict)) # debugging statement
            divMat[curPartElInd][get_key(connNode,ep_dict)]+=1
    # return matrix if desired
    if retMat:
        return divMat
    # otherwise return the divisor graph as a networkx object
    return nx.DiGraph(divMat)

def GetLocalSpec(G,ep_dict,lep_list):
    """return the spectrum of each LEP along with its divisor spectrum in a dictionary"""
    orig_spec = np.round(nx.adjacency_spectrum(G),3)
    GDiv = genDivGraph(G,ep_dict)
    GDivSpec = np.round(nx.adjacency_spectrum(GDiv),3)
    spec_dict = {} # dictionary to hold spectrums
    
    for lep in list(lep_list): # cycle through each lep
        node_list = []   # place to get all nodes in lep
        temp_ep_dict = {} # make a place for the original ep partitions
        
        # iterate through each partition element in that lep.
        for partElInd, partEl in enumerate(lep):
            node_list += ep_dict[partEl] # after this loop node_list has all nodes in the lep
            temp_ep_dict[partElInd] = ep_dict[partEl] # make the temporary ep_dict
                        
        subgraph = nx.subgraph(G,node_list) # make a subgraph
        spec_dict[f"{lep}"] = np.round(nx.adjacency_spectrum(subgraph),3)  # store its spectrum in dictionary
       
        spec_dict[f"{lep} Divisor"] = np.round(nx.adjacency_spectrum(genDivGraph(subgraph,temp_ep_dict)),3)
    spec_dict["Original Graph Divisor"] = GDivSpec
        
    return spec_dict, orig_spec

def NontrivialityData(G,ep_dict,lep_list, return_vals=False,plot=True,show_progress=True,verbose=False):
    """
    Gets the percentage of nodes in nontrivial eps and creates and returns a dictionary 
    with only the non-trivial partition elements leftover.

    Parameters
    ----------
    G (networkx graph):
        graph from which we dervied the ep_dict and lep_list
    ep_dict (dict):
        dictionary containing equitable partition elements and member nodes.
    lep_list (list):
        list containing sets with partition elements in each lep.

    Returns
    -------
    nontrivEp_dict
    nontrivLep_list
    nontrivNodePerc
    """
    from IPython.display import clear_output
    from matplotlib import pyplot as plt
    # Get the Equitable Partition elements that aren't trivial
    nontrivEp_dict={}
    hist_list = []
    lep_hist_list=[]
    totalPartEl = list(ep_dict.keys())[-1]
    pairs = 0
    
    # TODO: figure out how to only clear certain things
    if show_progress:
        print("Getting nontrivial ep elements...")
    for key,value in ep_dict.items():
        if show_progress:
            clear_output(wait=True)
            print(f"Nontrivial ep progress:{key}/{totalPartEl}")
        if len(value) == 2:
            nontrivEp_dict[key]=value
            pairs+=1
        elif len(value)!=1 and len(value)>2:
            nontrivEp_dict[key]=value
            hist_list.append(len(value))
            
    # this works for getting only the lep's that have lifting eigenvalues.
    if show_progress:
        print("Filtering leps to match...")
    nontrivLep_list = []
    total_in_leps = len(lep_list)
    for i,lep in enumerate(list(lep_list)):
        if show_progress:
            clear_output(wait=True)
            print(f"Filtering leps progress: {i}/{total_in_leps}")
        for element in lep:
            if element in nontrivEp_dict.keys():
                nontrivLep_list.append(lep)
                break
    
    # total nodes in nontrivial Leps
    if show_progress:
        print("getting total nontrivial nodes:")
    totalNontrivNodes = 0
    for i,ntLEP in enumerate(nontrivLep_list):
        lep_size=0
        if show_progress:
            clear_output(wait=True)
            print(f"Nontrivial nodes progress:{i}/{len(nontrivLep_list)}")
        for partEl in ntLEP:
            lep_size+=len(ep_dict[partEl])
            totalNontrivNodes+=len(ep_dict[partEl])
        if lep_size!=2:
            lep_hist_list.append(lep_size)
    
    if verbose:
        if totalNontrivNodes==0:
            print("executed here!!!")
            print("There were no nontrivial nodes...")
        else:
            pairs_perc = (2*pairs)/totalNontrivNodes
            print(f"Total percentage on nontrivial nodes: {totalNontrivNodes/len(G)}")
            print(f"Total pairs in partition elements: {pairs}")
            print(f"Percentage of nontrivial nodes in pairs: {pairs_perc}")

    if plot:
        pairs_perc = 1  # set as 1 since no nontrivial nodes mean
                        # there will be no nontrivial partition elements
        
        if pairs_perc==1:
            print("No nontrivial partition elements that aren't pairs, histograms blank")
        else:
            # get necessary data for bar graph
            ep_elements=list(set(hist_list))
            lep_elements = list(set(lep_hist_list))
            ep_presence=[]
            lep_presence=[]
            for size in ep_elements:
                ep_presence.append(hist_list.count(size))
            for size in lep_elements:
                lep_presence.append(lep_hist_list.count(size))
            # make bar graphs  
            plt.subplot(121)
            plt.bar(ep_elements,ep_presence,edgecolor='black')
            plt.xticks(np.arange(len(ep_elements)),ep_elements)
            plt.title("size of nontrivial EP elements")
            plt.ylabel("% of nontriv nodes in this size")
            
            plt.subplot(122)
            plt.bar(lep_elements,lep_presence,edgecolor='black')
            plt.title("size of nontrivial LEP elements")
            plt.xticks(np.arange(len(lep_elements)),lep_elements)
            plt.ylabel("% of nontriv nodes in this size")
            
            plt.tight_layout()
            plt.show()
            
    if return_vals:
        return nontrivEp_dict, nontrivLep_list,hist_list,totalNontrivNodes/len(G)
    


def relabel(G):
    mapping = {old_label: new_label for new_label, old_label in enumerate(G.nodes())}
    return nx.relabel_nodes(G, mapping)

def randomRelabel(G):
    new_labels = list(range(G.number_of_nodes()))
    random.shuffle(new_labels)
    mapping = {old_label: new_labels[i] for i, old_label in enumerate(G.nodes())}
    return nx.relabel_nodes(G, mapping)

